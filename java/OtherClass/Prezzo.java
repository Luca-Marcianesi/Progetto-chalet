public class Prezzo {
		
		
		private float prezzo;
		
		public Prezzo(float prezzo) {
			this.prezzo = prezzo;
		}
		
		public void setPrezzo ( float prezzo ) {  
			this.prezzo = prezzo;
		}
		
		
		public float getPrezzo () {
			return this.prezzo;
		}
		
		public String toString () {
			return ("Prezzo :" + prezzo );
		}
		
		
		
	}


